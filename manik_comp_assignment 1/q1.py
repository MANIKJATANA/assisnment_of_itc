#program to find the  average of 3 numbers!!
 
# taking input
num1=float(input("Enter the value of 1st number \n"))
num2=float(input("Enter the value of 2nd number \n"))
num3=float(input("Enter the value of 3rd number \n "))
 
# calculating average
avg=((num1+num2+num3)/3)
 
# printing output
print("The average of these numbers is \n", avg)